int main(char** argv, int argc)
{
    UNUSED(argv);
    UNUSED(argc);

    DX11::Engine engine;
    engine.Init();

    try
    {
        engine.Run();
    }
    catch (const std::exception& e)
    {
        // should be handled better
        std::cerr << e.what() << std::endl;
        DEBUG::log.Error(e.what());
        __debugbreak();
        return EXIT_FAILURE;
    }

    engine.ShutDown();

    return 0;
}